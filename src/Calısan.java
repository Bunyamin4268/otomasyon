
public class Calısan {
    private int id;
    private String Ürün_Adi;
    private String Ürün_Markasi;
    private String Ürün_Adeti;
    private String Ürün_Fiyati;

    public Calısan(int id, String Ürün_Adi, String Ürün_Markasi, String Ürün_Adeti, String Ürün_Fiyati) {
        this.id = id;
        this.Ürün_Adi = Ürün_Adi;
        this.Ürün_Markasi = Ürün_Markasi;
        this.Ürün_Adeti = Ürün_Adeti;
        this.Ürün_Fiyati = Ürün_Fiyati;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getÜrün_Adi() {
        return Ürün_Adi;
    }

    public void setÜrün_Adi(String Ürün_Adi) {
        this.Ürün_Adi = Ürün_Adi;
    }

    public String getÜrün_Markasi() {
        return Ürün_Markasi;
    }

    public void setÜrün_Markasi(String Ürün_Markasi) {
        this.Ürün_Markasi = Ürün_Markasi;
    }

    public String getÜrün_Adeti() {
        return Ürün_Adeti;
    }

    public void setÜrün_Adeti(String Ürün_Adeti) {
        this.Ürün_Adeti = Ürün_Adeti;
    }

    public String getÜrün_Fiyati() {
        return Ürün_Fiyati;
    }

    public void setÜrün_Fiyati(String Ürün_Fiyati) {
        this.Ürün_Fiyati = Ürün_Fiyati;
    }
    
    
} 